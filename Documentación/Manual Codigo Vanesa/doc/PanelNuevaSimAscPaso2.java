    public void construirConjuntos(Gramatica gramatica)  {        
        ArrayList<com.itextpdf.text.Paragraph> construccionConjuntoPrimero= new ArrayList();
        ArrayList<com.itextpdf.text.Paragraph> construccionConjuntoSiguiente= new ArrayList();
        ArrayList<NoTerminal> noTerminales= null;
        ArrayList<Terminal> conjuntoPrimero= null;
        ArrayList<Terminal> conjuntoSiguiente= null;
        ArrayList<Terminal> conjuntoPrimeroSimbolo= null;
        ArrayList<Terminal> conjuntoSiguienteSimbolo= null;        
        Iterator i$= null;
        NoTerminal noTerminal= null;
        String strConjuntoPrimero= null;
        String strConjuntoSiguiente= null;

        int i= 0;
        int j=0;

        this.modeloConjuntos =new DefaultTableModel();
        this.modeloConjuntos.addColumn("Simbolo");
        this.modeloConjuntos.addColumn("Conjunto primero");
        this.modeloConjuntos.addColumn("Conjunto siguiente");
        this.jTable1.setModel(this.modeloConjuntos);
        this.jTable1.getColumnModel().getColumn(0).setPreferredWidth(95);
        this.jTable1.getColumnModel().getColumn(1).setPreferredWidth(250);
        this.jTable1.getColumnModel().getColumn(2).setPreferredWidth(290);
        this.jTable1.setEnabled(false);

        noTerminales = gramatica.getNoTerm();
        
        i=0;
        while(i < noTerminales.size()){
            strConjuntoPrimero = "";
            strConjuntoSiguiente = "";
            noTerminal = noTerminales.get(i);
            conjuntoPrimero = noTerminal.getPrimeros();
            conjuntoSiguiente = noTerminal.getSiguientes();            
          
            j=0;
            while(j < conjuntoPrimero.size()){
                StringBuilder cadena = new StringBuilder();
                strConjuntoPrimero = cadena.append(strConjuntoPrimero).append(" ").append(conjuntoPrimero.get(j).getNombre()).toString();
               
                j++;
            }
            
            j=0;
            while(j < conjuntoSiguiente.size()){
                StringBuilder cadena = new StringBuilder();
                strConjuntoSiguiente = cadena.append(strConjuntoSiguiente).append(" ").append(conjuntoSiguiente.get(j).getNombre()).toString();
               
                j++;
            }                                            
            
            Object []  linea = new Object[]    
            {
              noTerminal.getValor(),          strConjuntoPrimero,          strConjuntoSiguiente
             };
            this.modeloConjuntos.addRow(linea);            
            i++;
        }
        this.ventanaPadre.gramatica = gramatica;        
    }                   

