    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        ArrayList<Produccion> prod = this.gramatica.getPr();
        int i=0;
        red++;
        
        if(red % 2 != 0 ){ //red es impar: completar con reducciones
            while(i < prod.size()){
                if(prod.get(i).getConsec().get(0).getNombre().equals("\u03b5")){
                    String ant = prod.get(i).getAntec().getSimboloNT().getNombre();
                    int j=0;
                    while(j < this.jTable1.getRowCount()){
                        if(this.jTable1.getValueAt(j, 0).toString().equals(ant)){
                            int k=1;
                            while(k < this.jTable1.getColumnCount()){
                                if(this.jTable1.getValueAt(j, k)==null){
                                     this.jTable1.setValueAt(i+1+"*", j, k);
                                }
                                k++;
                            }

                        }
                        j++;
                    }
                }
                i++;
            }
            this.jButton7.setText("Eliminar producciones Ã©psilon");
        }else{ //red par: Eliminar reducciones
           
           i=0;
           int j=1;
           
           while(i < this.jTable1.getRowCount()){                            
                j=1;
                while(j < this.jTable1.getColumnCount()){   
                    if(this.jTable1.getValueAt(i, j)!=null){
                        if(this.jTable1.getValueAt(i, j).toString().contains("*")){
                            this.jTable1.setValueAt(null, i, j);
                        }
                    }
                    j++;
                }  
                i++;
            }
            this.jButton7.setText("Rellenar con producciones Ã©psilon");
        }
        this.gramatica.setTPredictiva(this.tpredictiva);
        
    }//GEN-LAST:event_jButton7ActionPerformed

