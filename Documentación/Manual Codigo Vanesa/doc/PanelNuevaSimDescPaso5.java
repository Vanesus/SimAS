    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int columna = this.jTable1.getSelectedColumn();
        int fila = this.jTable1.getSelectedRow();
        this.jButton6.setEnabled(false);
        ArrayList<String> term = new ArrayList<>();
        term = buscarTerm();
        int i=0;
        int x=0;
        while(i<term.size()){
            if(this.tpredictiva.getCeldaPredictiva(fila, 0).toString().equals(term.get(i))){
                JOptionPane.showConfirmDialog(null, "Esta fila no se puede rellenar, debido a que el sÃ­mbolo \""+term.get(i)+"\" no aparece en la parte derecha de una produccion en un lugar que no sea el primero.", "Salir",JOptionPane.CLOSED_OPTION);
                //Esta fila no se puede rellenar, debido a que el sÃ­mbolo "+term.get(i)+" aparece el primero en una de las producciones.", "Salir",JOptionPane.CLOSED_OPTION);
                x=-1;
                break;
            }else
                i++;
        }
        if(x==0){       
            if(this.tpredictiva.getCeldaPredictiva(fila, columna) == null){            
                String str = "E"+this.jComboBox1.getSelectedItem().toString().substring(0, 1);            
                this.tpredictiva.setCeldaPredictiva(str, fila, columna);            
             //   this.jTable1.setDefaultRenderer (Object.class, new MiRender());
            }
            if(this.tpredictiva.getCeldaPredictiva(fila, columna).toString().startsWith("E")){
                this.jButton6.setEnabled(true);
            }
            this.gramatica.setTPredictiva(this.tpredictiva); 
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private ArrayList<String> buscarTerm(){        
        ArrayList<String> term = new ArrayList<>();
        int i=0;
        while(i< this.gramatica.getPr().size()){
            if(this.gramatica.isTerminal(this.gramatica.getPr().get(i).getConsec().get(0).getNombre()))
                term.add(this.gramatica.getPr().get(i).getConsec().get(0).getNombre());
            i++;
        }
        return term;
    }
    
