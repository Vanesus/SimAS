    private void anadirTerminales(){
        ArrayList<Terminal> term = this.ventanaPadre.getGramatica().getTerm();
        DefaultTableModel tabla = this.tpredictiva.getTabla();
        int fila = this.tpredictiva.getTabla().getRowCount();
        int columna = 1;
        int i=0;
        
        while(i < term.size()){            
            Object []  linea = new Object[]  {
                  term.get(i).getNombre()
                 };
            tabla.addRow(linea);     
            
            i++;
        }        
        Object []  linea = new Object[]  {
                  "$"
                 };
            tabla.addRow(linea);
            
        this.tpredictiva.setTabla(tabla);
        i = fila;
        while(i < tabla.getRowCount()-1){                        
            this.tpredictiva.setCeldaPredictiva("<HTML> <FONT COLOR=\"blue\">Emparejar</FONT></HTML>", i, columna);
            i++;
            columna++;            
        }
        this.tpredictiva.setCeldaPredictiva("<HTML> <FONT COLOR=\"blue\">Aceptar</FONT></HTML>", i, columna);
    }
   
