     private void inicializarCombos(TablaPredictiva tpredictiva) {
        ArrayList<String> listaCombo = new ArrayList(); 
        ArrayList<FuncionError> funError = tpredictiva.getFunError();
        StringBuilder string = new StringBuilder();
        int i= 0;
          
        while(i < funError.size()) { 
            string = new StringBuilder();
            int accion;
            string = string.append(funError.get(i).getIdentificador());
            string = string.append(" - ");
            accion = funError.get(i).getAccion();
            if(accion == 1)
                string.append("Insertar un SÃ­mbolo en la Entrada: ");
            if(accion == 2)
                string.append("Borrar un SÃ­mbolo de la Entrada");
            if(accion == 3)
                string.append("Modificar un SÃ­mbolo de la Entrada: ");
            if(accion == 4)
                string.append("Insertar un SÃ­mbolo de la Pila: ");
            if(accion == 5)
                string.append("Borrar un SÃ­mbolo de la Pila");
            if(accion == 6)
                string.append("Modificar un SÃ­mbolo de la Pila: ");
            if(accion == 7)
                string.append("Terminar el anÃ¡lisis");
            if(accion == 1 || accion ==3 || accion ==4 || accion ==6)
                string.append(funError.get(i).getSimbolo().getNombre());
            
            listaCombo.add(string.toString());                   
            i++;            
        }        
        DefaultComboBoxModel combo = new DefaultComboBoxModel(listaCombo.toArray());                
        this.jComboBox1.setModel(combo);                
    }

