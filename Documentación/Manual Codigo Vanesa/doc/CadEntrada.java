    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String cadena = this.jTextField1.getText();
        String [] separado = cadena.split(" ");
        ArrayList<Terminal> cadenaEntrada = new ArrayList<>();
        int i = 0;
        while(i < separado.length){            
            cadenaEntrada.add(new Terminal(separado[i],separado[i]));            
            i++;
        }
        if(this.nuevo != null){
            this.nuevo.ventanaPadre.setCadenaEntrada(cadenaEntrada);
            this.nuevo.actualizarVisualizacion();
        }
        if(this.nuevoAsc != null){
            this.nuevoAsc.ventanaPadre.setCadenaEntrada(cadenaEntrada);
            this.nuevoAsc.actualizarVisualizacion();
        }
            
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

