    public CadEntrada(NuevaSimulacionDesc nuevo) {
        initComponents();
        this.nuevo = nuevo;
        this.jTextField1.setText("");
        ArrayList<Terminal> cadenaEntrada = this.nuevo.ventanaPadre.getCadenaEntrada();
        String aux = "";
        int i=0;
        DefaultListModel terminales = new DefaultListModel();
        terminales = this.nuevo.gramatica.getTerminales();             
        this.jList1.setModel(terminales);
        
        if(cadenaEntrada != null){
            while (i < cadenaEntrada.size()){
                aux = aux+cadenaEntrada.get(i).getNombre()+" ";
                i++;
            }
        }
        this.jTextField1.setText(aux);        
    }
    
