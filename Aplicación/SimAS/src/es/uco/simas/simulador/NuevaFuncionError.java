    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ArrayList<FuncionError> funciones = new ArrayList();
        if(this.sim ==1)
            funciones = this.gramatica.getTPredictiva().getFunError();
        if(this.sim ==2)
            funciones = this.gramatica.getTlr().getTAccion().getFunError();
        
        int i=0;
        if("".equals(this.jTextField1.getText().toString())){
            JOptionPane.showConfirmDialog(null, "El identificador no puede estar vacío.", "Salir",JOptionPane.CLOSED_OPTION);
        }else{
            int j=0;
            while(i < funciones.size()){                
                if(funciones.get(i).getIdentificador()==Integer.parseInt(this.jTextField1.getText())){
                    JOptionPane.showConfirmDialog(null, "El identificador está repetido.", "Salir",JOptionPane.CLOSED_OPTION);
                    j=1;
                    break;
                }else
                    i++;
            }        

            if(j==0) {
                int id = Integer.parseInt(this.jTextField1.getText().toString());
                int accion = this.jComboBox1.getSelectedIndex()+1;
                String mensaje = this.jTextField2.toString();
                Terminal term = new Terminal(this.jComboBox2.getSelectedItem().toString(),this.jComboBox2.getSelectedItem().toString());                
                FuncionError funError = new FuncionError(id,accion,mensaje ); 
                funError.setSimbolo(term);
                if(sim==1){
                    this.tPredictiva.crearFunError(funError);
                    this.gramatica.setTPredictiva(tPredictiva);
                    if(this.paso4 != null)
                        this.paso4.funcionError();
                }
                if(sim==2){
                    ParteAccion ac = this.tlr.getTAccion();
                    ac.crearFunError(funError);
                    this.tlr.setTAccion(ac);
                    this.gramatica.setTlr(this.tlr);                    
                    if(this.paso5 != null)
                        this.paso5.funcionError();
                }                               
                this.dispose();
            }
        }                
        
    }//GEN-LAST:event_jButton2ActionPerformed

