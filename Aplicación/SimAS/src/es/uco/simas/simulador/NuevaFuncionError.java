    private void inicializarCombos() {
        ArrayList<String> listaCombo = new ArrayList();                               
        DefaultListModel terminales= new DefaultListModel();
        int i= 0;
        terminales = this.gramatica.getTerminales();
       
        while(true) {
            if(i >= terminales.size())  {
                break;
            }
            if(i < terminales.size()) {
                listaCombo.add((String)terminales.get(i));                
                i++;
            }
        }        
        DefaultComboBoxModel combo = new DefaultComboBoxModel(listaCombo.toArray());                
        this.jComboBox2.setModel(combo);        
        
    }
      
